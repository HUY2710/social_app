class Post {
  final int id;
  final String avatar;
  final String name;
  final String time;
  final String image;
  final String title;
  final String subtitle;
  final int views;
  final int comments;
  final int loves;

  Post({
    required this.id,
    required this.avatar,
    required this.name,
    required this.time,
    required this.image,
    required this.title,
    required this.subtitle,
    required this.views,
    required this.comments,
    required this.loves,
  });
}
